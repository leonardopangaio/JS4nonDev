let n1, n2, n3, n4
n1 = 10
n2 = 5
n3 = 15
n4 = 2

//Logical operator AND example.
if((n1>n2)&&(n3>n4)){
    console.log("Verdadeiro.")
}else{
    console.log("Falso.")
}

//Logical operator OR example.
if((n1>n2) || (n4>n3)){
    console.log("Verdadeiro.")
}else{
    console.log("Falso.")
}

//Logical operator NOT example.
if(!(n2>n1) && (n3>n4)){
    console.log("Verdadeiro.")
}else{
    console.log("Falso.")
}